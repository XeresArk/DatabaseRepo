DROP TABLE HACKATHON_SEARCH_RESULT;
CREATE TABLE HACKATHON_SEARCH_RESULT (
    HSR_ID INT AUTO_INCREMENT PRIMARY KEY,
    RUN_ID INT,
    OBJ_TYPE VARCHAR(20),
    SEARCH_DESC VARCHAR(4000),
    OWNER_NAME VARCHAR(30),
    TABLE_NAME VARCHAR(40),
    COLUMN_NAME VARCHAR(40),
    CREATED_BY VARCHAR(10),
    CREATED_DATE DATETIME DEFAULT CURRENT_TIMESTAMP,
    LAST_EDITED_BY VARCHAR(10),
    LAST_EDITED_DATE DATETIME
);